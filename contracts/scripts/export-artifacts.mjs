// Exports compiled ABIs + creation bytecode into the web app as `as const`
// TypeScript literals, so viem/wagmi infer function names, args, and return
// types end-to-end. Run after `npm run compile`.
import { readFile, writeFile, mkdir } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const here = path.dirname(fileURLToPath(import.meta.url));
const artifactsDir = path.join(here, "..", "artifacts", "contracts");
const outDir = path.join(here, "..", "..", "web", "src", "contracts", "generated");

const CONTRACTS = [
  { name: "Counter", file: "Counter.sol" },
  { name: "GuestBook", file: "GuestBook.sol" },
  { name: "TipJar", file: "TipJar.sol" },
  { name: "Create2Factory", file: "Create2Factory.sol" },
  { name: "DelegateLogic", file: "DelegateDemo.sol" },
  { name: "DelegateDemo", file: "DelegateDemo.sol" },
  { name: "AssemblyLab", file: "AssemblyLab.sol" },
];

await mkdir(outDir, { recursive: true });

const indexLines = ["// AUTO-GENERATED by contracts/scripts/export-artifacts.mjs — do not edit.\n"];

for (const { name, file } of CONTRACTS) {
  const artifactPath = path.join(artifactsDir, file, `${name}.json`);
  const artifact = JSON.parse(await readFile(artifactPath, "utf8"));
  const camel = name[0].toLowerCase() + name.slice(1);

  const content = [
    `// AUTO-GENERATED from contracts/artifacts — do not edit. Recompile + re-export instead.`,
    `export const ${camel}Abi = ${JSON.stringify(artifact.abi, null, 2)} as const;`,
    ``,
    `export const ${camel}Bytecode = ${JSON.stringify(artifact.bytecode)} as const;`,
    ``,
  ].join("\n");

  await writeFile(path.join(outDir, `${camel}.ts`), content);
  indexLines.push(`export { ${camel}Abi, ${camel}Bytecode } from "./${camel}";`);
  console.log(`exported ${name}`);
}

await writeFile(path.join(outDir, "index.ts"), indexLines.join("\n") + "\n");
console.log(`\nwrote ${CONTRACTS.length} contracts to ${outDir}`);
